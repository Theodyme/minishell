#include "libft.h"

size_t	ft_putnbr(long long nb, size_t i)
{
	if (nb < 0)
	{
		nb = -nb;
		write(1, "-", 1);
		i++;
	}
	if (nb >= 10)
		i = ft_putnbr(nb / 10, ++i);
	nb = (nb % 10) + '0';
	write(1, &nb, 1);
	return (i);
}
